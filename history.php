<h1> History </h1>

<pre class="content">



The Coders club was born under the co-founders: Katherine Moloney and Thomas “Chuck” Griffin. Their 
idea was to create an organization at the City College of San Francisco where students can share advice 
about programming, be social and get together to hack.

The first meeting held on August 27th, 2012. Online presence was established early on, 
ensuring ongoing contact between club members and alumni.

Spring 2013 was first semester for the Coders club to be officially recognized by CCSF . Meetings
were often held alongside the Linux Users Group (LUG), a club that Coders will always have close 
ties with. Katherine Moloney was the first president of the Coders club, followed by Thomas “Chuck” 
Griffin as the second president in Fall 2013.

Even though both co-founders are both employed full-time in the tech industry, they continually give 
back to Coders, providing insight and mentoring new leaders for programming careers in the San Francisco Bay Area.

Furthermore, Katherine established the Women-Who-Code club at CCSF, modeled after the 
Women-Who-Code-SF in order to support women interested in computer science at our community college.

Lastly, the Coders club will always strive to motivate students to embrace the bustling tech community 
we live in, giving back to it whenever possible.

Coding is fun but coding together is awesome!


